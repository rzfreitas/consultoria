		<p>Todavia, como para cada país de língua diferente existiria uma sigla, para gerar a universalização do termo criou-se um padrão internacional de nomenclatura. Portanto, em qualquer lugar do planeta a organização é conhecida como o termo oficial: ISO. Outro fator que contribuiu para a definição é a expressão em grego isos que corresponde a “igual”, o que se enquadra com o propósito da organização.</p>		

		<p>Com sede em Genebra, na Suíça, a ISO foi criada em 1947. Seu principal objetivo de fundação foi desenvolver e promover normas de qualidade que possam ser adotadas por empresas do mundo inteiro. Atualmente a Organização reúne participantes em mais de 200 países.</p>
		
		<p>Como a economia está em constantes mudanças, as normas recebem adequações a cada cinco anos, aproximadamente. Cada uma das normas é focada em um nicho específico do mercado. Nos últimos anos, as adaptações foram realizadas para alcançar o máximo de empresas possíveis, de grandes corporações até microempresas.</p><p>A representante brasileira da ISO no Brasil é a <a href='http://www.abnt.org.br'><strong>Associação Brasileira de Normas Técnicas (ABNT)</strong></a>.</p>	
