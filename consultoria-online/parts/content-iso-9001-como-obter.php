	<h2 class="font-verde text-center"> O que é ISO 9001? </h2>
	<div class="text-justify">
		<p>A <a href='https://www.iso.org/home.htm'><strong>Organização ISO</strong></a> possui uma série de normas relacionadas a vários temas, como a família <a href='https://www.consultoriaiso.org/iso-14001/o-que-e-iso-14001'><strong>ISO 14000</strong></a> ligada às questões ambientais. Ela especifica os requisitos do Sistema de Gestão Ambiental e permite a organização desenvolver e praticar políticas e metas ambientalmente sustentáveis.</p>
		
		<p>Para implantar o sistema de gestão e conquistar a certificação em Segurança de Alimentos, existe a norma <a href='https://www.consultoriaiso.org/iso-22000/o-que-e-iso-22000/'><strong>ISO 22000</strong></a>. Há outras normas relevantes e que abordam outros temas essenciais no mercado. Todavia a ISO 9001 é a norma mais significativa da organização.</p>
		
		<p>A ISO 9001 pertence à família 9000 e está relacionada a gerenciamento e garantia da qualidade da empresa. Nesta família estão contidas as seguintes ISO´s: 9000, 9002, 9002, 9003 e 9004. A mais abrangente é a ISO 9001 que incorpora todos os 20 elementos de qualidade da norma.</p>
		
		<p>A partir da <a href='https://www.consultoriaiso.org/o-que-significa-nbr-iso-9001/'><strong>NBR ISO 9001</strong></a>, versão brasileira da norma, organização pode apontar a qualidade nos processos da empresa, o que infere consequentemente na qualidade dos serviços ou produtos por ela oferecidos.</p>
		
