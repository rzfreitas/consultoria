	<h2 class="font-verde text-center">A ISO 14001 na versão 2015!</h2><br>
        <div class="row">
            <div class="small-12 medium-12 large-6 columns text-justify">		

                <p>Ao longo dos anos, tanto as empresas quanto a estrutura do mercado sofrem diversos tipos de transformações. Na tentativa de tornar as normas flexíveis e atuais a estas mudanças, a ISO promove adequações e inserções com regulamentos praticáveis e adaptáveis a toda e qualquer organização. A <strong>ISO 14001</strong> é uma das normas que sofreu ajustes em seus requisitos. Ela conta com a nova versão desde o ano de 2015.
Uma das principais adequações na <strong>ISO 14001:2015</strong> é o alinhamento ao conceito sustentabilidade. As empresas certificadas terão maiores resultados e benefícios com o sistema de gestão ambiental. Outra alteração é a centralidade na liderança como meio de atingir os objetivos do sistema. Com a diretoria envolvida, é mais fácil colocar em prática os requisitos e objetivos da norma. 
Outros ajustes estão relacionados ao entendimento do contexto da organização, ou seja, a observação das necessidades e a expectativa das partes interessadas. A ênfase em uma abordagem de riscos e o destaque ao fortalecimento do desempenho ambiental da organização também fazem parte das mudanças sofridas na <strong>ISO 14001</strong>.
Neste sentido a <strong>ISO 14001</strong> versão 2008 está ultrapassada há aproximadamente dois anos. 
</p>               

