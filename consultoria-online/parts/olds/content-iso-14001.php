Nesse sentido, seja pela preocupação com a imagem ou pelas multas exorbitantes que podem gerar um desastre ambiental, empresas inteligentes devem se preocupar em manter a gestão ambiental em plena conformidade. Ser ecologicamente correto passou a ser uma necessidade e não apenas um interesse das organizações que desejam se manter à frente no mercado.
