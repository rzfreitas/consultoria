    <h2 class="font-verde text-center"> Benefícios da certificação ISO 14001 </h2><br>
        
        <div class="small-12 columns text-center p-b-50 p-b-c">
            <img src="https://i.imgur.com/SIWdPfN.jpg" alt="Benefícios da certificação ISO 14001"></img>
        </div>

     <div class="row">

        <div class="small-12 medium-12 large-12 columns text-justify">

            <p>Contar com um sistema de gestão ambiental conhecido e reconhecido no mundo inteiro envolve uma série de vantagens diante do mercado, da sociedade e do público interno da organização.
            Vejamos abaixo outros benefícios da certificação <strong>ISO 14001</strong>.

            <li><strong>Aperfeiçoamento do Sistema de Gestão Ambiental</strong></li>
            A norma aprimora os itens da gestão ambiental existentes dentro da empresa. Proporciona o aperfeiçoamento da política ambiental interna e as adaptações necessárias para o crescimento saudável da empresa sem agressões severas ao meio ambiente. 
            <li><strong>Crescimento eficaz</strong></li>
            Um dos requisitos que regem o sistema de gestão <strong>ISO 14001</strong> é a redução de gastos desnecessários durante os processos de produção da empresa e a redução de desperdícios. Portanto, a empresa funciona com mais eficiência e sem grandes perdas financeiras.
            <li><strong>Aumento da rentabilidade</strong></li>
            Com a queda nos gastos com energia e resíduos, melhora a rentabilidade da empresa como um todo. 
            A redução dos custos prevenindo poluição e diminuição de gastos com descartes de produtos, também são outras vantagens da <strong>ISO 14001</strong> diante da contenção dos gastos.
            <li><strong>Melhora na imagem da empresa</strong></li>
            Muitas organizações aderem ao uso do selo sustentável junto a marca como meio de informarem ao mercado que estão atuando de modo ecologicamente correto. Uma serie de conceitos positivos são atrelados a imagem da empresa como: transparência, responsabilidade ambiental e a ideia de uma organização “limpa”, sem prejuízos ao meio ambiente.
            Várias oportunidades de negócios podem surgir pela presença da certificação. Haja vista, que os empresários desejam manter relações comerciais com empresas confiáveis, que não possam viver futuros escândalos, como já aconteceram na história de diversas empresas brasileiras. 
            Citemos como exemplo: grandes derramemos de petróleo no mar, queda nas barragens, poluição significativa do ar e da água, entre outros. 
            <li><strong>Cumprimento da legislação ambiental</strong></li>
            Em situações como catástrofes ambientais decorrentes de erros das indústrias ou empresas de produção, as multas podem alcançar valores exorbitantes. Há casos de penalidades na casa dos bilhões. Além da imagem danificada, muitas destas empresas chegam a ter que fechar as portas pelos gastos onerosos.
            A certificação na <strong>ISO 14001</strong> traz não apenas a grandes corporações, mas também a pequenos empreendimentos o conhecimento da legislação em vigor no que se refere a sua área de atuação e também as principais orientações que devem ser adotadas para evitar problemas deste nível. 
            Portanto, ter o selo ambiental não significa apenas ser uma empresa ecologicamente correta, mas evitar prejuízos e multas desnecessárias por agressão a natureza.  
            É importante frisar que o sucesso da norma não está apenas na conquista da certificação, mas a continuidade do cumprimento de cada um dos requisitos estabelecidos pela <strong>ISO 14001</strong>. 
            <li><strong>Incentivo ao cumprimento de ações voltadas para a gestão ambiental</strong></li>
            A norma de gestão ambiental estimula a organização a superar a morosidade das ações ambientais. Incentiva a empresa a retirar os objetivos do papel, tornando-os metas reais e palpáveis. 
            <li><strong>Competitividade internacional</strong></li>
            Muitas empresas internacionais exigem como forma de fechamento de contrato a certificação em algumas normas, entre elas a <strong>ISO 14001</strong>. Pela representatividade internacional, a norma abre um leque de possibilidades comerciais, além da legitimidade que a empresa ganha no serviço ou produto. 
            Empresas que prezam por valores ambientais preferem manter relações com outras corporações que possuem a mesma diretriz ambiental. Nesse sentido, a norma de gestão de qualidade favorece a marca e traz valores sustentáveis a empresa. 
            <li><strong>Satisfação do Cliente</strong></li>
            Receber um produto ou serviço de qualidade cumprindo a legislação ambiental promove tanto a satisfação do cliente, quanto a positividade da marca. É de interesse das pessoas tanto pela conduta moral, quanto pela consciência social que o produto ou serviço que elas usufruem seja devidamente produzido ou seja ecologicamente correto. 
            </p>

