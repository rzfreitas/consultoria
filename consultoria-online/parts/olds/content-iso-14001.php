	<h2 class="small-12 columns font-verde text-center">  Mas o que é preciso fazer para se ter uma empresa ecologicamente correta? </h2><br>

    <div class="small-12 medium-12 large-12 columns text-justify">		

        <p> A <a href="https://www.iso.org/">ISO</a>, Organização Internacional de Normalização, traz uma série de normas para proporcionar qualidade nos mais diversos níveis de atuação de uma empresa. Dentre elas está a <strong>família 14000</strong>.  
A <strong>série 14000</strong> traz os requisitos presentes no Sistema de Gestão Ambiental. A intenção é que qualquer empresa, seja ela de grande ou pequeno porte possa desenvolver uma estrutura que minimize o máximo possível os impactos sob o meio ambiente. 
A família 14000 abarca os seguintes pontos: sistemas de gestão ambiental, auditorias ambientais, rotulagem ambiental, comunicação ambiental, análise do ciclo de vida, desempenho ambiental, aspectos ambientais e terminologia.
Para você conhecer melhor a <strong>família 14000</strong>, apresentaremos algumas das normas mais relevantes: 
</p>

  <h3><strong>ISO 14001</strong></h3>
  <p>Aborda os principais requisitos para as empresas identificarem, controlarem e monitorarem as questões ambientais. A norma se baseia em um sistema de gestão ambiental. É uma das únicas normas da <strong>família 14000</strong> que traz o selo ambiental. A ISO 14001 possui maior expressividade entre as outras e geralmente é a primeira a ser implementada quando se está à procura de gestão de qualidade.</p>

  <h3><strong>ISO 14004</strong></h3>
  <p>Ela faz referência a <strong>ISO 14001</strong> provendo requisitos adicionais para implantação do SGA. A norma é focada na utilização interna da organização, portanto funciona como uma espécie de suporte da gestão ambiental;</p>

  <h3><strong>ISO 14010</strong></h3>
  <p>São normas sobre as Auditorias Ambientais. Elas funcionam como um meio de trazer credibilidade e seguridade a todo processo de implementação e certificação ambiental. Proporcionam também ações como a verificação dos compromissos estabelecidos pela empresa em seu sistema de gestão;</p>

  <h3><strong>ISO 14031</strong></h3>
  <p>Funciona como guia para análise de desempenho ambiental;</p>

  <h3><strong>ISO 14020</strong></h3>
  <p>Conjunto de normas que tratam de selos ambientais;</p>

  <h3><strong>ISO 14040</strong></h3>
  <p>Conjunto de normas utilizadas para orientar os diagnósticos de ciclo de vida de produtos e serviços;</p>

  <h3><strong>ISO 14064</strong></h3>
  <p>Contabilização e verificação de emissões de gases de efeito estufa para suportar projetos de redução de emissões;
Há também outras normas da família focadas em outros tópicos relevantes da gestão ambiental, como a ISO 14065, 14064, 14064 dentre outras. Para outras informações mais específicas sobre cada uma das normas da <strong>família 14000</strong>, entre em contato com um dos especialistas da <a href="https://www.consultoriaiso.org/">Consultoria Online Verde Ghaia</a> pelo telefone <b>(31) 3656-5538</b>. 
Como explicado anteriormente, dentre as normas do sistema de gestão ambiental, a <strong>ISO 14001</strong> é a mais ampla e a mais utilizada pelas empresas do mundo todo. No Brasil, a norma ganhou a versão brasileira, <strong>NBR ISO 14001</strong>. Ela é representada pela <a href="http://www.abnt.org.br/">Associação Brasileira de Normas Técnicas (ABNT)</a> que é um dos membros fundadores da entidade. 
A <strong>ISO 14001</strong> estipula o comprometimento das organizações frente a prevenção da poluição e com melhorias contínuas, como parte do ciclo normal de gestão empresarial. A norma é baseada no ciclo PDCA do inglês "plan-do-check-act" - planejar, fazer, checar e agir - e utiliza terminologia e linguagem de gestão conhecida.
A <strong>ISO 14001</strong> apresenta uma série de benefícios a organização. Tendo em vista, que toda empresa visa lucros, as ações de preservação ou de redução dos impactos ao meio ambiente devem ser realizadas com uma política sustentável que não prejudique o crescimento econômico da empresa.
Nesse sentido apresentamos abaixo, os principais aspectos positivos que a norma proporciona as empresas certificadas.
</p>

