	<h2 class="font-verde text-center"> Atualização da norma de qualidade, versão ISO 9001:2015 </h2><br>

    <div class="small-12 medium-12 large-12 columns text-justify">		

        <p>Criada em 1987, a <strong>ISO 9001</strong> tem passado por adaptações e ajustes a cada cinco anos, aproximadamente. As mudanças realizadas pela Organização ISO se devem a tentativa de manter a norma do sistema de gestão de qualidade atualizada e flexível o suficiente para atender as demandas e manter um padrão compatível com as necessidades atuais do mercado.</p>

        <p>A última versão foi lançada no ano de 2015 e traz mudanças profundas em suas diretrizes. Todas as empresas certificadas na versão anterior, do ano de 2008, deverão fazer a migração o quanto antes para os requisitos recentes. Caso as organizações não migrem até setembro de 2018, terão as certificações canceladas.</p>

        <p>Destacamos que caso a sua empresa ainda não tenha o selo da <strong>ISO 9001</strong>, ao implementar os requisitos e passar pelo processo de auditoria, a certificação já vem atualizada.</p>

        <p>Vejamos abaixo as principais mudanças da nova versão da <strong>ISO 9001</strong> e de que maneira elas poderão influenciar o seu negócio: </p>

        <h3><strong>Não obrigatoriedade do Manual da Qualidade</strong></h3>
        <p>Um dos requisitos da <strong>ISO 9001</strong> tratava-se da obrigatoriedade do Manual de Qualidade. Era necessário que o documento fosse mantido dentro da organização de forma impressa e acessível aos colaboradores.  Com a <a href="https://www.consultoriaiso.org/iso-9001/migracao-para-empresas-certificadas-iso-9001-2015/" target="_blank"><strong>ISO 9001:2015</strong></a>, não é mais necessário manter o Manual de Qualidade. A relevância do documento continua com a mesma relevância, no entanto ele foi inserido em outros requisitos.</p>

        <p>A nova versão parte do pressuposto que o manual de qualidade não é um processo burocrático que a empresa deve manter em algum espaço, mas são práticas que devem fazer parte da cultura e na forma de funcionamento da empresa. Outra razão também para esta mudança é tornar as ações e decisões menos burocráticas, acelerando os processos e otimizando o funcionamento da organização.</p>

        <h3><strong>Envolvimento da liderança</strong></h3>
        <p>Para o sucesso e cumprimento dos requisitos é necessário o envolvimento diretoria da empresa. Na nova versão, o papel principal não deve mais ser ocupado por um líder do projeto de implementação da<strong>ISO 9001:2015</strong>, mas pelas lideranças da empresa. A ideia é que a cultura da gestão de qualidade alcance todos os níveis da corporação. O conhecimento da norma deve ser difundido de maneira clara e eficiente, tornando o SGQ parte da empresa.</p>

        <h3><strong>Redução de 8 para 7 princípios da qualidade</strong></h3>
        <p>Dois requisitos da norma sofrerão uma espécie de junção. Portanto, os itens passam de 8 para 7. A ideia é facilitar a compreensão do procedimento, e diminuir a burocratização.  Outro ajuste foi a adaptação da linguagem, tornando-a mais clara e de fácil compreensão. </p>

        <h3><strong>Notificação e resolução das reclamações</strong></h3>
        <p>A norma de gestão de qualidade sempre teve com um dos focos, a ouvidoria ao cliente, mas com a nova <strong>versão 2015 da ISO 9001</strong>, prioriza-se também outras partes interessadas como fornecedores, funcionários, entre outros. A ideia é registrar e tratar adequadamente cada uma das observações, desde que elas estejam alinhadas aos requisitos do Sistema de Gestão de Qualidade</p>

        <h3><strong>Plano de comunicação</strong></h3>
        <p>Para fortalecer tanto o cumprimento dos itens de qualidade pela empresa, quanto para conhecimento do escopo da <strong>ISO 9001:2015</strong> é necessário a divulgação. Na nova versão, prioriza-se a comunicação como um dos meios de fortalecimento do SGQ na cultura das empresas. Neste sentido, a empresa deverá manter um plano de comunicação interna e externa, definindo papéis, responsabilidades e normas de conduta.</p>

        <h3><strong>Substituição do termo “treinamento” por “conhecimento”</strong></h3>
        <p>Com o objetivo de fortalecer a sustentabilidade do negócio a longo, prazo, os treinamentos, presentes na versão antiga, continuam, mas ganham uma relevância ainda maior. Na <strong>ISO 9001:2015</strong>, passam a ser conhecidos como gestão do conhecimento da empresa. </p>

        <h3><strong>Registros e controles de documentos passam a ser “informação documentada”</strong></h3>
        <p>Uma vez que os princípios da qualidade passam a ser a decisão baseada em informações, nada mais natural do que substituir os controles e registros de documentos pelo termo “informação documentada”. Esse é outro item que compõe a gestão do conhecimento organizacional e facilita a tomada de decisão da empresa.</p>

        <p>Agora que você já conhece as principais informações do <strong>ISO 9001:2015</strong>, basta aprender a maneira correta de implementá-la. Destacamos que o processo de implementação é diferente do processo de auditoria para certificação.</p>

